<script type="text/javascript">



document.onkeydown = keydown;
      function keydown(evt){
        if (!evt) evt = event;
           if (evt.keyCode==13){ //enter
               if($('#username').val!=''&& $('#password').val()!='' && $('#property').val()!=''){
                    login();
               }else{
                $('#password').focus();
               }
            }
       }

function login(){
  var username=$('#username').val();
  var passwd=$('#password').val();
  var property=$('#property').val();
    if(property==''){
    swal("Error", "Select the Company!", "error");
    }
  else if(username==''){
    swal("Error", "Enter your Username!", "error");
    }
    else  if(passwd==''){
    swal("Error", "Enter your Password!", "error");
    }
    
    else {
     $('#message').html('<img id="img-spinner" src="img/spin.gif" style="margin-top:0px;width:20px" alt="Loading"/>');
        $.ajax({
        url:'login.php',
        data:{username:username, passwd:passwd,property:property},
        success:function(data){
          if(data==0){
           swal("Success", "Login successful.Redirecting....", "success");
            window.location.href = "main.php";
          }else $('#message').html(data);
          }
          });
    }
}

$('#username').focus();
</script>
